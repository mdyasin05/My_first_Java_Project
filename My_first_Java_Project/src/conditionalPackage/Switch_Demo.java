package conditionalPackage;

public class Switch_Demo {

	public static void main(String[] args) {
		
		int x=4;
		
		switch(x)
		{
       
		case 1:
			System.out.println("performance is poor");
		break;
		
		case 2:
			System.out.println("slow peroformance");
		break;
		
		case 3:
			System.out.println("meeting expectation");
		break;
		
		case 4:
			System.out.println("Well done Keep Rocking");
			break;
			
		case 5:
			System.out.println("Employ of the Year");
		break;
		
		default:
			System.out.println("You r fired");
	}
	}
}
